package org.icipe.epfa.modeldesigner;

public class ImageProperties {
	
	private String corrX1, corrX2, corrY1, corrY2, mini, maxi, legX, legY, scaleY, scaleX;
	private String labX, labY, title;
	
	public ImageProperties(){}
	
	public String getCorrX1() {
		return corrX1;
	}
	public void setCorrX1(String corrX1) {
		this.corrX1 = corrX1;
	}
	public String getCorrX2() {
		return corrX2;
	}
	public void setCorrX2(String corrX2) {
		this.corrX2 = corrX2;
	}
	public String getCorrY1() {
		return corrY1;
	}
	public void setCorrY1(String corrY1) {
		this.corrY1 = corrY1;
	}
	public String getCorrY2() {
		return corrY2;
	}
	public void setCorrY2(String corrY2) {
		this.corrY2 = corrY2;
	}
	public String getMini() {
		return mini;
	}
	public void setMini(String mini) {
		this.mini = mini;
	}
	public String getMaxi() {
		return maxi;
	}
	public void setMaxi(String maxi) {
		this.maxi = maxi;
	}
	public String getLegX() {
		return legX;
	}
	public void setLegX(String legX) {
		this.legX = legX;
	}
	public String getLegY() {
		return legY;
	}
	public void setLegY(String legY) {
		this.legY = legY;
	}
	
	public void setScaleY(String scaleY) {
		this.scaleY = scaleY;
	}
	public String getScaleX() {
		return scaleX;
	}

	public void setScaleX(String scaleX) {
		this.scaleX = scaleX;
	}
	public String getScaleY() {
		return scaleY;
	}

	public String getLabX() {
		return labX;
	}
	public void setLabX(String labX) {
		this.labX = labX;
	}
	public String getLabY() {
		return labY;
	}
	public void setLabY(String labY) {
		this.labY = labY;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	

}
